package com.iotassistant.controllers.dtos;

public class EnableDTO {
	
	private boolean enable;
	

	public EnableDTO() {
		super();
	}

	public EnableDTO(boolean enable) {
		super();
		this.enable = enable;
	}

	public boolean isEnable() {
		return enable;
	}

	public void setEnable(boolean enable) {
		this.enable = enable;
	}
	
	

}
